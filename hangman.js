let obj = {
    word: wordList[Math.floor(Math.random() * 582)],
    input: document.querySelector(".word-input"),
    tryBtn: document.querySelector(".btn__tryLetter"),
    livesEl: document.querySelector('.lives'),
    btnContainerEl: document.querySelector(".btn__container"),
    lives: 7,

    resetLetters: function() { 
        obj.lettersArr.forEach(element => { 
        element.style.opacity = '0'
        
    })},

    showLetters: function() { 
        obj.lettersArr.forEach(element => { 
        element.style.opacity = '1'
    })},

    resetGame: function() {
        window.location.reload()
    }
}

obj.livesEl.innerText = ('Lives = ' + obj.lives)

for(let i = 0; i < obj.word.length; i++)    
    document.querySelector('.words__container').innerHTML += 
    `<div class="words__word"> <span class = "words__span"> ${obj.word[i]} </span> </div>`
obj.lettersArr = Array.from(document.querySelectorAll(".words__span"))

obj.resetLetters()

// Button
obj.tryBtn.addEventListener('click', () => {
    if(obj.input.value)
    checkForLetter(obj.input.value)
})

function checkForLetter(inputValue) {
    if (obj.word.includes(inputValue)) { 
        for (span of obj.lettersArr) 
        if (inputValue === span.innerText) {
            span.style.opacity = "1"
        }
        
        //When won
        let correctLetterCount = 0
        for (i in obj.lettersArr) {
            if (obj.lettersArr[i].style.opacity == "1")
            correctLetterCount++;

            if(correctLetterCount === obj.word.length){
                obj.livesEl.innerText = "YOU WIN!"
                obj.btnContainerEl.innerHTML = `<button class="btn__tryAgain">Try Again</button>`
                let tryAgainBtn = document.querySelector(".btn__tryAgain")
        
                tryAgainBtn.addEventListener('click', function resetGame(){
                    obj.resetGame()
                    })
                }
            }
            
        }

    else{
    obj.lives -= 1
    }
    obj.livesEl.innerText = ('Lives = ' + obj.lives)
    
    // When Lost:
    if (obj.lives === 0){
        obj.livesEl.innerText = "YOU LOST!"
        obj.btnContainerEl.innerHTML = `<button class="btn__tryAgain">Try Again</button>`
        let tryAgainBtn = document.querySelector(".btn__tryAgain")
        obj.showLetters()

        tryAgainBtn.addEventListener('click', function resetGame(){
            obj.resetGame()
            })
    }
}

